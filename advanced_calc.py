num_1 = int(input("Ingresa el primer número: "))
num_2 = int(input("Ingresa el segundo número: "))

div = num_1 // num_2
cuadrado_num_1 = num_1 ** 2
cubo_num_1 = num_1 ** 3

cuadrado_num_2 = num_2 ** 2
cubo_num_2 = num_2 ** 3

modulo = num_1 % num_2

print('La div exacta entre ambos numeros es: ',div)
print('Cuadrado del numero 1 es: ', cuadrado_num_1)
print('Cubo del numero 1 es: ', cubo_num_1)
print('Cuadrado del numero 2 es: ', cuadrado_num_2)
print('Cubo del numero 2 es: ', cubo_num_2)
print('Modulo de ambos numeros es: ', modulo)

